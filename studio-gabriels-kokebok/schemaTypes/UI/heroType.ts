import { defineField, defineType } from 'sanity'

export default defineType({
  name: 'hero',
  title: 'Hero',
  type: 'document',
  fields: [
    defineField({
      name: 'title',
      title: 'Title',
      type: 'string',
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'subtitle',
      title: 'Subtitle',
      type: 'string',
    }),
    defineField({
      name: 'featuredRecipe',
      title: 'Featured Recipe',
      type: 'reference',
      to: [{ type: 'recipe' }],
      validation: (Rule) => Rule.required(),
    }),
  ],
})

